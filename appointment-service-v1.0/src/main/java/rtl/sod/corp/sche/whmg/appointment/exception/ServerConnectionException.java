package rtl.sod.corp.sche.whmg.appointment.exception;

public class ServerConnectionException extends Exception {
	private static final long serialVersionUID = -7588162242134713854L;

	public ServerConnectionException() {
	}

	public ServerConnectionException(String arg0) {
		super(arg0);
	}

	public ServerConnectionException(Throwable arg0) {
		super(arg0);
	}

	public ServerConnectionException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServerConnectionException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
